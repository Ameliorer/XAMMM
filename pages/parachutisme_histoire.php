<h1> L'histoire du parachutisme</h1>

<p>Le parachute est un dispositif destiné à freiner le mouvement, principalement vertical d'un objet ou d'un individu dans les airs. Historiquement, le parachute sert à rendre possible le retour au sol en bonne condition d'une personne abandonnant un aéronef, mais le terme a été appliqué à de nombreux dispositifs destinés à empêcher une chute ou à ralentir un appareil au moyen d'un dispositif semblable à un parachute de descente.</p>

<p> <span>Etymologie :</span> Le mot parachute est constitué du préfixe para- (du latin paro (« parer, contrer »)) et du mot français chute (fait de tomber) : protection contre la chute. Le mot a été inventé par Louis-Sébastien Lenormand en 1785.</p>

<h2>Léonard de Vinci a imaginé le parachute</h2>

<p> 
Le parachute a été conçu par Léonard de Vinci en 1485.
Il s'agit d'un parachute pyramidal d'une hauteur latérale d'environ 7 mètres.
Ce n'est qu'en 2008 que le premier saut avec une réplique du parachute de Léonard de Vinci a été réalisé par le parachutiste suisse Olivier Vietti-Teppa .Largué en hélicoptère d'une hauteur de 650 mètres, parachuté à 600 mètres et il a atterri sans encombre.
</p> 

<h1>Le parachute de Garnerin</h1>

<h2>Le premier saut par Garnerin</h2>
<p> 
Le premier saut en parachute a été effectué en 1797 par André Jacques Garnerin au-dessus d'un parc à Paris.
Puis un siècle s'est déroulé avant que le parachute ne soit utilisé.
Ce n'est qu'à partir des années 1940 qu'il se développe significativement puis devient un sport.
</p> 
<h2>Le parachutisme militaire</h2>
<p> 
Pendant la Première Guerre mondiale, le parachute a été utilisé comme dispositif d'urgence.
Depuis la Seconde Guerre mondiale, il est utilisé par les troupes aéroportées.
C'était un largage à basse altitude avec des cercles dont la conception n'était pas très différente du parachute Garnerin.
</p> 
<h1>Le parachute aile</h1>

<h2>Free-fly</h2>
<p> 
L'avènement des parachutes "ailés" dans les années 1980 a considérablement accru la sécurité et la liberté des parachutistes civils en les aidant à mieux naviguer et atterrir.
Grâce aux avions plus efficaces, les largages se font à une hauteur suffisamment élevée pour profiter de la phase de chute libre que la plupart des élèves apprécient.
C'est ainsi que les nouvelles disciplines ont vu : Vol Relatif, Freestyle et Free Fly, Air Surfing, Landing et Contact Rowing répondre au besoin grandissant de nouvelles sensations.
</p> 
<h2>La sécurité</h2>
<p> 
Aujourd’hui la Fédération Française de Parachutisme compte plus de 7000 adhérents réguliers.
Le parachutisme est un sport en pleine expansion.Nous pouvons par exemple compter  plus de 22 000 sauts de découverte et de débutants par an.
Cette augmentation du nombre de participants va de pair avec le développement important des mesures de sécurité.
</p> 
<h2>Avenir du parachutisme</h2>
<p> 
Le parachutisme n'est plus une activité extrême réservée aux marginalisés, mais un sport à part entière.
Ceci est soutenu par une étude portant sur l'intégration des disciplines olympiques et la réalisation de sauts de démonstration aux Jeux olympiques de 2000 à Sidney.

Depuis 2008, des parachutistes débarquent sur les Champs Élysées le 14 juillet.
Ces images, vues par des millions de personnes, donnent au sport une image très positive et le valorisent ainsi.
</p> 
